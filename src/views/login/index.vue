<template>
  <div class="login-container">
    <el-form class="login-form" autoComplete="on" :model="loginForm" :rules="loginRules" ref="loginForm" label-position="left">
      <h3 class="title">净水管理后台</h3>
      <el-form-item prop="username">
        <span class="svg-container svg-container_login">
          <svg-icon icon-class="user" />
        </span>
        <el-input name="username" type="text" v-model="loginForm.username" autoComplete="on" placeholder="用户名" />
      </el-form-item>
      <el-form-item prop="password">
        <span class="svg-container">
          <svg-icon icon-class="password"></svg-icon>
        </span>
        <el-input name="password" :type="pwdType" v-model="loginForm.password" autoComplete="on"
          placeholder="密码"></el-input>
          <span class="show-pwd" @click="showPwd"><svg-icon icon-class="eye" /></span>
      </el-form-item>
      <el-form-item prop="verifyCode">
        <el-input name="verifyCode" maxlength="4" v-model="loginForm.verifyCode" @keyup.enter.native="handleLogin" placeholder="验证码" />
        <span style="text-align: center;">
          <img style="display: inline-block; vertical-align: middle; width:60px" @click="refreshVerifyCode" v-bind:src="vcodeSrc" />
        </span>
      </el-form-item>
      <el-form-item>
        <el-button type="primary" style="width:100%;" :loading="loading" @click.native.prevent="handleLogin">
          登入
        </el-button>
      </el-form-item>
    </el-form>
  </div>
</template>

<script>

export default {
  name: 'login',
  data() {
    return {
      vcodeSrc: process.env.BASE_API + '/api/passport/getVerifyCode',
      loginForm: {
        username: '',
        password: '',
        verifyCode: ''
      },
      loginRules: {
        username: [{ required: true, message: '用户名不能为空', trigger: 'blur' }],
        password: [{ required: true, message: '秘密不能为空', trigger: 'blur' }],
        verifyCode: [{ required: true, message: '验证码不能为空', trigger: 'blur' }]
      },
      loading: false,
      pwdType: 'password'
    }
  },
  methods: {
    refreshVerifyCode() {
      this.vcodeSrc = process.env.BASE_API + '/api/passport/getVerifyCode?t=' + new Date()
    },
    showPwd() {
      if (this.pwdType === 'password') {
        this.pwdType = ''
      } else {
        this.pwdType = 'password'
      }
    },
    handleLogin() {
      this.$refs.loginForm.validate(valid => {
        if (valid) {
          this.loading = true
          this.$store.dispatch('Login', this.loginForm).then(() => {
            this.loading = false
            this.$router.push({ path: '/' })
          }).catch(() => {
            this.loading = false
            this.vcodeSrc = process.env.BASE_API + '/api/passport/getVerifyCode?t=' + new Date()
          })
        } else {
          console.log('error submit!!')
          return false
        }
      })
    }
  }
}
</script>

